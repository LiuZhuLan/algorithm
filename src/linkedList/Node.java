package linkedList;

/**
 * 链表
 * @author LiuZhulan
 */
public class Node{

    private Integer item;
    private Node next;

    public Integer getItem() {
        return item;
    }

    public void setItem(Integer item) {
        this.item = item;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }
}
